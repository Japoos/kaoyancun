package com.kaoyancun.entity;

public class DirectionSchool {

    private Integer dispensingId;
    private String schoolName;
    private String provence;
    private Integer dispensingType;

    public Integer getDispensingId() {
        return dispensingId;
    }

    public void setDispensingId(Integer dispensingId) {
        this.dispensingId = dispensingId;
    }

    public String getSchoolName() {
        return schoolName;
    }

    public void setSchoolName(String schoolName) {
        this.schoolName = schoolName;
    }

    public String getProvence() {
        return provence;
    }

    public void setProvence(String provence) {
        this.provence = provence;
    }

    public Integer getDispensingType() {
        return dispensingType;
    }

    public void setDispensingType(Integer dispensingType) {
        this.dispensingType = dispensingType;
    }
}
